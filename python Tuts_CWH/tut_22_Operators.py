
# operators in python :
# 1. Arithmetic operator
# 2. Assignment operator
# 3. Comparison operator
# 4. Logical operator
# 5. Identity operator
# 6. Membership operator
# 7. Bitwise operator

# Arithmetic operator ( +, -, *, /, %, ** )
print("5 + 6 is", 5 + 6)
print("5 - 6 is", 5 - 6)
print("5 * 6 is", 5 * 6)
print("5 / 6 is", 5 / 6)
print("5 % 6 is", 5 % 6)
print("5 **  6 is", 5 ** 6)

# Assignment operator ( =, +=, -= )
x = 5
print(x)
x += 5
print(x)

# Comparison operator ( ==, <, >, <=, >=, != )
i = 5
print(i == 7)
print(i < 5)
print(i > 5)
print(i <= 5)
print(i >= 5)
print(i != 5)
print(i < 5)

# Logical operator ( and, or, nor )
a = True
b = False

print(a and b)
print(a or b)

# Identity operator ( is , is not )
x = 100
y = 400

print(x is y)
print(x is not y)

# Membership operator ( in , not in )
list1 = [145, 78, 256, 896, 900, 2365]

print(145 in list1)
print(845712 not in list1)

# Bitwise operator
# 0 - 00
# 1 - 01
# 2 - 10
# 3 - 11

print(0 & 1)
print(0 | 2)



